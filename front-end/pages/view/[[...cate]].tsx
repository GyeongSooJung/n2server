import type { GetServerSideProps, NextPage } from "next";
import Head from "next/head";
import Footer from "../../src/components/layout/Footer";
import Header from "../../src/components/layout/Header";
import SignUp from "../../src/components/page/SignUp";
import Main from "../../src/components/page/Main";
import Find from "../../src/components/page/Find";
import { parseJwt } from "../../src/modules/parseJwt";
import { CompanyApproval } from "../../src/models/company.entity";

interface ViewProps {
  cate: any;
}

const Componentitem: NextPage<any> = (props) => {
  const cate = props.cate;
  const main = cate[0];
  const sub = cate[1] ? cate[1] : "";

  switch (main) {
    case "signup":
      return <SignUp />;
    case "main":
      return <Main />;
    case "find":
      return <Find />;
    default:
      return <SignUp />;
  }
};

const View: NextPage<ViewProps> = ({ cate }) => {
  return (
    <div>
      <Head>
        <title>MK SOLUTION</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div
        style={{ display: "flex", flexDirection: "column", minHeight: "100vh" }}
      >
        <Header {...cate} />
        <Componentitem {...cate} />
        <Footer />
      </div>
    </div>
  );
};

export default View;

export const getServerSideProps: GetServerSideProps = async (context) => {
  const cate = context.query;
  const main = cate.cate ? cate.cate[0] : null;

  // 토큰 확인 - 없을 경우, 로그인 화면으로 리디렉트
  if (!context.req.cookies.mk_token) {
    if (main !== "signup" && main !== "find") {
      return {
        redirect: {
          permanent: false,
          destination: "/",
        },
      };
    } else {
      return {
        props: {
          cate,
        },
      };
    }
  } else {
    const tokenValue = parseJwt(context.req.cookies.mk_token);
    const cApproval = tokenValue.cApproval;
    if (
      cApproval === CompanyApproval.BEFORE ||
      cApproval === CompanyApproval.ING
    ) {
      return {
        redirect: {
          permanent: false,
          destination: "/",
        },
      };
    } else {
      return {
        props: {
          cate,
        },
      };
    }
  }
};
